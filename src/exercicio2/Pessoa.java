package exercicio2;

/**
 * 
 * @author Matheus Gadelha
 * Classe que representa uma pessoa
 */
public class Pessoa {
	
	private String nome;
	private Integer idade;
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public String getNome() {
		return nome;
	}
	
	public void setIdade(Integer idade) {
		this.idade = idade;
	}
	
	public Integer getIdade() {
		return idade;
	}

}
